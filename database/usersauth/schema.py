from datetime import datetime

from pydantic import BaseModel

from database.user.schema import User


class UserAuthBase(BaseModel):
    pass


class UserAuthLogin(BaseModel):
    login: str
    password: str


class UserAuth(UserAuthBase):
    token: str
    created: datetime
    user_id: int
    user: User
    state: int
    meta: str

    class Config:
        orm_mode = True
        arbitrary_types_allowed = True
