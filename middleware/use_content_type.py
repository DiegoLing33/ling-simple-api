from requests import Request


def use_content_type(app):
    """
    Applies the middleware
    :param app:
    :return:
    """
    @app.middleware("http")
    async def json_encoding_header(request: Request, call_next):
        """
        Encodes json to UTF-8
        :param request:
        :param call_next:
        :return:
        """
        url = str(request.url)
        response = await call_next(request)
        if '/docs' in url:
            pass
        else:
            response.headers["content-type"] = "application/json;charset=utf-8"
        return response
